nome = "Guilherme Arthur de Carvalho"

print(nome[0]) #mostra o primeiro caractere G

print(nome[:9]) #Do inicio até o indice 9(exclui o 9): Guilherme

print(nome[10:]) #Do indice 10 até o fim: Arthur carvalho

print(nome[10:16]) #Do 10 ao 15

print(nome[10:16:2]) #Do 10 ao 15 pulando de 2 em 2 letras: Art

print(nome[:5]) #Do incio até o indice 5 (exclui o 5): Guilh

print(nome[-1]) #ultimo caractere da string: o





